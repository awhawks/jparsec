	/**
	 * L1 Ephemerides v1.2. Output vector is in equatorial coordinates, in
	 * AU and AU/day. Tchebycheff polynomials are used to represent very long
	 * period perturbations, hence limiting the interval of validity of
	 * ephemerides to about [-810,810] years centered on J1950. Outside this
	 * range these polynomials are not used and ephemerides can be calculated,
	 * but results are approximate.
	 *
	 * @param ET Julian day in TDB.
	 * @param ks Satellite ID from 1 to 4 (Io to Callisto).
	 * @return Jovicentric J2000 vector (x, y, z, vx, vy, vz).
	 */
	public static double[] L1_theory(double ET, int ks)
	{
		double ome = 6.24950183065715;
		double ainc = 0.445094736497665;

		double T = ET - 2433282.5; // J1950

		double elem[] = new double[7];
		double TN[] = new double[9];
		double val[] = new double[6];

		/*
		*  (IMCCE) computing corrections to elements from Tchebycheff polynomials :
		*  These corrections represent very long period perturbations which
		*  are not computed in form of trigonometric terms but only approximated
		*  by Tchebycheff polynomials, hence limiting the interval of validity
		*  of ephemerides to about [-810,810] years centered on J1950.
		*
		*  To compute approximate ephemerides at longer time, skip
		*  these corrections by giving a .false. value to AccurateEphem
		*/
		boolean accurateEphem = true;
		if (Math.abs(T / 365.25) > 810.0) accurateEphem = false;
		if (accurateEphem) {
			double T1 = -819.727638594856D+0, T2 = 812.721806990360D+0;
	        double a = T1;
	        double b = T2;
	        double x = (T / 365.25 - 0.5 * (b + a)) / (0.5 * (b - a));
			if (Math.abs(x) <= 1.0) {
		        TN[0] = 1.0;
		        TN[1] = x;
		        for (int it=2; it<=8; it++) {
		          TN[it] = 2.0 * x * TN[it-1] - TN[it-2];
		        }
		        for (int nv=1; nv<=5; nv++) {
			        for (int it=0; it<=8; it++) {
			        	val[nv] = val[nv] + c[ks][it+1][nv] * TN[it];
			        }
		          val[nv] = val[nv] - 0.5 * c[ks][1][nv];
		        }
			}
		}

		/*
		*  computing elements from series
		*/
        int kv = 1;
        double s = 0.0;
        for (int k=1; k<=nbterm[ks][kv]; k++) {
		     double arg = phas[k][ks][kv] + freq[k][ks][kv] * T;
		     s = s + ampl[k][ks][kv] * Math.cos(arg);
        }
		elem[1] = s;

		kv=2;
		s = al[1][ks] + al[2][ks] * T;
        for (int k=1; k<=nbterm[ks][kv]; k++) {
        	double arg = phas[k][ks][kv] + freq[k][ks][kv] * T;
			s = s + ampl[k][ks][kv] * Math.sin(arg);
        }
		s = (s + val[1]) % Constant.TWO_PI;
		if (s < 0) s += Constant.TWO_PI;
		elem[2] = s;

		kv = 3;
		double s1 = 0.0;
		double s2 = 0.0;
        for (int k=1; k<=nbterm[ks][kv]; k++) {
        	double arg = phas[k][ks][kv] + freq[k][ks][kv] * T;
			s1 = s1 + ampl[k][ks][kv] * Math.cos(arg);
			s2 = s2 + ampl[k][ks][kv] * Math.sin(arg);
        }
		elem[3] = s1 + val[2];
		elem[4] = s2 + val[3];

		kv = 4;
        s1 = 0.0;
        s2 = 0.0;
        for (int k=1; k<=nbterm[ks][kv]; k++) {
        	double arg = phas[k][ks][kv] + freq[k][ks][kv] * T;
			s1 = s1 + ampl[k][ks][kv] * Math.cos(arg);
			s2 = s2 + ampl[k][ks][kv] * Math.sin(arg);
        }
	    elem[5] = s1 + val[4];
		elem[6] = s2 + val[5];

		/*
		*  computing cartesian coordinates from elements
		*/
		double XV[] = ELEM2PV(MU[ks], elem);

		/*
		*  then transforming to cartesian coordinates in the
		*      J2000 mean equator and mean equinox reference frame
		*/
		elem[1] = XV[1] * Math.cos(ome) - XV[2] * Math.sin(ome) * Math.cos(ainc) + XV[3] * Math.sin(ainc) * Math
				.sin(ome);
		elem[2] = XV[1] * Math.sin(ome) + XV[2] * Math.cos(ome) * Math.cos(ainc) - XV[3] * Math.sin(ainc) * Math
				.cos(ome);
		elem[3] = XV[2] * Math.sin(ainc) + XV[3] * Math.cos(ainc);
		elem[4] = XV[4] * Math.cos(ome) - XV[5] * Math.sin(ome) * Math.cos(ainc) + XV[6] * Math.sin(ainc) * Math
				.sin(ome);
		elem[5] = XV[4] * Math.sin(ome) + XV[5] * Math.cos(ome) * Math.cos(ainc) - XV[6] * Math.sin(ainc) * Math
				.cos(ome);
		elem[6] = XV[5] * Math.sin(ainc) + XV[6] * Math.cos(ainc);

		return new double[]	{ elem[1], elem[2], elem[3], elem[4], elem[5], elem[6] };
	}

	private static double[] ELEM2PV(double MU, double[] ELEM)
	{
		double K = ELEM[3]; // Eccentricity * sin(periapsis long)
		double H = ELEM[4]; // E * cos (periap lon)
		double Q = ELEM[5]; // tan(i/2) * cos (node)
		double P = ELEM[6]; // tan(i/2) * sin (node)
		double A = ELEM[1]; // Semimajor axis
		double AL = ELEM[2]; // Mean longitude
		double AN = Math.sqrt(MU / (A * A * A)); // Mean motion, MU = G*M1*(1+M2/M1)
		double EE = AL + K * Math.sin(AL) - H * Math.cos(AL);
		double DE = 1.0;

		do
		{
			double CE = Math.cos(EE);
			double SE = Math.sin(EE);
			DE = (AL - EE + K * SE - H * CE) / (1.0 - K * CE - H * SE);
			EE = EE + DE;
		} while (Math.abs(DE) >= 1.0E-12);

		double CE = Math.cos(EE);
		double SE = Math.sin(EE);
		double DLE = H * CE - K * SE;
		double RSAM1 = -K * CE - H * SE;
		double ASR = 1.0 / (1.0 + RSAM1);
		double PHI = Math.sqrt(1.0 - K * K - H * H);
		double PSI = 1.0 / (1.0 + PHI);
		double X1 = A * (CE - K - PSI * H * DLE);
		double Y1 = A * (SE - H + PSI * K * DLE);
		double VX1 = AN * ASR * A * (-SE - PSI * H * RSAM1);
		double VY1 = AN * ASR * A * (CE + PSI * K * RSAM1);
		double F2 = 2.0 * Math.sqrt(1.0 - Q * Q - P * P);
		double P2 = 1.0 - 2.0 * P * P;
		double Q2 = 1.0 - 2.0 * Q * Q;
		double PQ = 2.0 * P * Q;

		double XV[] = new double[]
		{ 0.0, X1 * P2 + Y1 * PQ, X1 * PQ + Y1 * Q2, (Q * Y1 - X1 * P) * F2, VX1 * P2 + VY1 * PQ, VX1 * PQ + VY1 * Q2,
				(Q * VY1 - VX1 * P) * F2 };

		return XV;
	}
	
